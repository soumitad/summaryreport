package com.hughes.summary;


public class Report {

	private int totalSite;
	private int uniqueSiteOwners;
	private int uniqueBigOwner;
	private int bigOwnerSiteCounts;
	private int otherBigOwner;
	private int otherBigOwnerSiteCounts;
	public int getTotalSite() {
		return totalSite;
	}
	public void setTotalSite(int totalSite) {
		this.totalSite = totalSite;
	}
	public int getUniqueSiteOwners() {
		return uniqueSiteOwners;
	}
	public void setUniqueSiteOwners(int uniqueSiteOwners) {
		this.uniqueSiteOwners = uniqueSiteOwners;
	}
	public int getUniqueBigOwner() {
		return uniqueBigOwner;
	}
	public void setUniqueBigOwner(int uniqueBigOwner) {
		this.uniqueBigOwner = uniqueBigOwner;
	}
	public int getBigOwnerSiteCounts() {
		return bigOwnerSiteCounts;
	}
	public void setBigOwnerSiteCounts(int bigOwnerSiteCounts) {
		this.bigOwnerSiteCounts = bigOwnerSiteCounts;
	}
	public int getOtherBigOwner() {
		return otherBigOwner;
	}
	public void setOtherBigOwner(int otherBigOwner) {
		this.otherBigOwner = otherBigOwner;
	}
	public int getOtherBigOwnerSiteCounts() {
		return otherBigOwnerSiteCounts;
	}
	public void setOtherBigOwnerSiteCounts(int otherBigOwnerSiteCounts) {
		this.otherBigOwnerSiteCounts = otherBigOwnerSiteCounts;
	}
}
